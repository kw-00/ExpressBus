using Microsoft.CodeAnalysis;
using System.Text;

namespace ExpressBus.Protocol.SourceGeneration.Message;

/// <summary>
/// Generates a partial struct for types annotated with <c>[Message]</c>.
/// </summary>
/// <remarks>
/// For each validated <c>[Message]</c> struct this produces:
/// <list type="bullet">
///   <item><description>A partial struct declaration with the appropriate interfaces.</description></item>
///   <item><description><c>MessageTypeIdentifier</c> — the unique byte tag assigned by the generator.</description></item>
/// </list>
/// <para>
/// For <c>[Message]</c>-only types, this class also generates
/// <c>ByteSize</c>, constructor, <c>FromBytes</c>, and <c>ToBytes</c>.
/// For types with <c>[GenerateSerialization]</c>, <c>SerializationGeneration</c>
/// generates those members instead.
/// </para>
/// </remarks>
internal static class MessageGeneration
{
	private static readonly string[] GlobalUsings =
	[
		"global using global::System;",
		"global using global::System.Buffers;",
		"global using global::ExpressBus.Protocol;",
		"global using global::ExpressBus.Buffering;",
	];

	/// <summary>
	/// Generates a partial struct for a single <c>[Message]</c> type.
	/// </summary>
	/// <param name="symbol">The message struct symbol.</param>
	/// <param name="identifier">The sequential MessageTypeIdentifier (0..N).</param>
	/// <param name="compilation">The compilation for semantic analysis.</param>
	/// <param name="messageType">The MessageType enum value for the type.</param>
	/// <param name="isSerializationType">True if the type also has [GenerateSerialization] — additional members come from SerializationGeneration.</param>
	public static string GenerateMembersForSingleType(
		SyntaxSymbol symbol,
		byte identifier,
		Compilation compilation,
		MessageType messageType,
		bool isSerializationType)
	{
		var semanticModel = compilation.GetSemanticModel(symbol.Declaration.SyntaxTree);
		var props = SerializedPropResolver.ParseSerializedPropAttributes(symbol, semanticModel).ToList();
		var namespaceName = symbol.Symbol.ContainingNamespace.ToDisplayString();
		var typeName = symbol.Symbol.Name;

		var sb = new StringBuilder();
		sb.AppendLine("// <auto-generated />");
		foreach (var usingDirective in GlobalUsings)
			sb.AppendLine(usingDirective);
		sb.AppendLine();
		sb.AppendLine($"namespace {namespaceName}");
		sb.AppendLine("{");

		if (!isSerializationType)
		{
			// [Message]-only type: generate everything including all interfaces.
			sb.AppendLine($"\tpublic partial struct {typeName} : IByteSerializable<{typeName}>, IMessage, IMessageSize");
			sb.AppendLine("\t{");

			// MessageTypeIdentifier — static for type-level dispatch.
			sb.AppendLine($"\tpublic static byte MessageTypeIdentifier => {identifier};");

			// ByteSize — 1 byte MessageTypeIdentifier, no additional fields.
			sb.AppendLine("\tpublic int ByteSize => 1;");

			GenerateConstructor(sb, typeName, []);
			GenerateFromBytesMethod(sb, typeName, []);
			GenerateToBytesMethod(sb, typeName, []);

			sb.AppendLine("\t}");
		}
		else
		{
			// [GenerateSerialization] type: only declare interfaces and MessageTypeIdentifier.
			// Serialization members come from SerializationGeneration.
			sb.AppendLine($"\tpublic partial struct {typeName} : IByteSerializable<{typeName}>, IMessage, IMessageSize");
			sb.AppendLine("\t{");

			// MessageTypeIdentifier — static for type-level dispatch.
			sb.AppendLine($"\tpublic static byte MessageTypeIdentifier => {identifier};");

			sb.AppendLine("\t}");
		}

		sb.AppendLine("}");

		return sb.ToString();
	}

	private static void GenerateConstructor(
		StringBuilder sb,
		string typeName,
		IReadOnlyList<MessagePropInfo> props)
	{
		sb.AppendLine();
		var params_ = props.Select(p =>
		{
			var paramType = p.EnumType ?? p.UnderlyingType.CSharpTypeName();
			return $"{paramType} {ToCamelCase(p.Name)}";
		});
		sb.AppendLine($"\tpublic {typeName}({string.Join(", ", params_)})");
		sb.AppendLine("\t{");
		foreach (var prop in props)
		{
			var assignment = prop.EnumType is not null
				? $"({prop.EnumType}){ToCamelCase(prop.Name)}"
				: ToCamelCase(prop.Name);
			sb.AppendLine($"\t\t{prop.Name} = {assignment};");
		}
		sb.AppendLine("\t}");
	}

	private static void GenerateFromBytesMethod(
		StringBuilder sb,
		string typeName,
		IReadOnlyList<MessagePropInfo> props)
	{
		sb.AppendLine();
		sb.AppendLine($"\tpublic static {typeName} FromBytes(Memory<byte> buffer)");
		sb.AppendLine("\t{");
		sb.AppendLine($"\t\tByteTools.EnsureLength(buffer.Span, default({typeName}).ByteSize);");
		sb.AppendLine("\t\tvar reader = new ByteReader(buffer.Span.Slice(1));");
		sb.AppendLine();

		foreach (var prop in props)
		{
			var readMethod = prop.UnderlyingType.ReadMethodName();
			var paramName = ToCamelCase(prop.Name);
			var varType = prop.EnumType ?? prop.UnderlyingType.CSharpTypeName();
			var cast = prop.EnumType is not null ? $"({varType})" : "";
			sb.AppendLine($"\t\t{varType} {paramName} = {cast}reader.{readMethod}();");
		}

		sb.AppendLine();
		sb.AppendLine($"\t\treturn new {typeName}({string.Join(", ", props.Select(p => ToCamelCase(p.Name)))});");
		sb.AppendLine("\t}");
	}

	private static void GenerateToBytesMethod(
		StringBuilder sb,
		string typeName,
		IReadOnlyList<MessagePropInfo> props)
	{
		sb.AppendLine();
		sb.AppendLine($"\tpublic ReadOnlyMemory<byte> ToBytes(Memory<byte> buffer)");
		sb.AppendLine("\t{");
		sb.AppendLine($"\t\tByteTools.EnsureLength(buffer.Span, default({typeName}).ByteSize);");
		sb.AppendLine("\t\tvar writer = new ByteWriter(buffer.Span);");
		sb.AppendLine("\t\twriter.WriteByte(MessageTypeIdentifier);");
		sb.AppendLine();

		foreach (var prop in props)
		{
			var writeMethod = prop.UnderlyingType.WriteMethodName();
			string cast = prop.EnumType is not null
				? $"({prop.UnderlyingType.CSharpTypeName()})"
				: "";
			sb.AppendLine($"\t\twriter.{writeMethod}({cast}{prop.Name});");
		}

		sb.AppendLine();
		sb.AppendLine("\t\treturn buffer;");
		sb.AppendLine("\t}");
	}

	private static string ToCamelCase(string name) =>
		char.ToLowerInvariant(name[0]) + name.Substring(1);
}
